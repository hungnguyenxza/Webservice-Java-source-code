    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String pID = JOptionPane.showInputDialog("Nhap productID:");
        if(pID==null || pID.equals("")){
            return;
        }
        ProductDTO p = DAO.loadProductByID(pID);
        if (p != null) {
            txtID.setText(p.getProductID());
            txtName.setText(p.getProductName());
            txtDate.setText(sdf.format(new Date(p.getCreatedDate())));
            comboboxModel.setSelectedItem(new ComboboxData(p.getManufacturerID(), p.getManufacturerName()));
            return;
        }
        this.setTitle("Khong co Product can tim");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        
        this.setTitle("");
        String manuID = JOptionPane.showInputDialog("Nhap ManuID:");
        if(manuID==null || manuID.equals("")){
            return;
        }
        List<ProductDTO> list = DAO.loadProductByManuID(manuID);
        if (list.size() > 0) {
            loadAllProducts(list);
            return;
        }
        this.setTitle("Khong co Product nao");

    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        this.setTitle("");
        boolean check = false, result;
        if (!txtID.getText().equals("")) {
            check = true;
        }
        if (check) {
            result = DAO.deleteProduct(txtID.getText());

        } else {
            String pID = JOptionPane.showInputDialog("Nhap ProductID de xoa:");
            result = DAO.deleteProduct(pID);
        }
        if (result) {
            this.setTitle("Xoa thanh cong");
            loadAllProducts(DAO.loadAllProducts());
        } else {
            this.setTitle("Khong co Product de xoa");
        }

    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            this.setTitle("");
            ProductDTO p = new ProductDTO();
            p.setProductID(txtID.getText());
            p.setProductName(txtName.getText());
            p.setCreatedDate(sdf.parse(txtDate.getText()).getTime());
            ComboboxData cb = (ComboboxData) comboboxModel.getSelectedItem();
            p.setManufacturerID(cb.value);
            p.setManufacturerName(cb.label);
            boolean result = DAO.addNewProduct(p);
            if (result) {
                this.setTitle("Them thanh cong");
                loadAllProducts(DAO.loadAllProducts());
            } else {
                this.setTitle("Da co loi xay ra");
            }
        } catch (ParseException ex) {
            Logger.getLogger(Manager.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

