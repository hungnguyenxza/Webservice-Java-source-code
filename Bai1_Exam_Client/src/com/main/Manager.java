    private void loadAllManufacturer() {
        for (ManufacturerDTO it : DAO.loadAllManufacturers()) {
            comboboxModel.addElement(new ComboboxData(it.getManufacturerID(), it.getManufacturerName()));
        }
    }

    private void loadAllProducts(List<ProductDTO> loadAllProducts) {
        tableModel.setRowCount(0);
        Object[] data;
        for (ProductDTO it : loadAllProducts) {
            data = new Object[]{it.getProductID(), it.getProductName(), sdf.format(new Date(it.getCreatedDate())), it.getManufacturerName()};
            tableModel.addRow(data);
        }
    }

    class ComboboxData {

        private String label;
        private String value;

        public ComboboxData(String value, String label) {
            this.label = label;
            this.value = value;
        }

        public String getValue() {
            return value;
        }

        @Override
        public String toString() {
            return label;
        }

    }
